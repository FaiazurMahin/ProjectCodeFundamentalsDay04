//Task01: Write a program to print numbers from 1 to 10 using
//for
function num(){
    for(let i=1; i<=10; i++){
        console.log(i);
    }
}
num();
//while
function numb(){
    let i = 1;
    while(i<=10){
        console.log(i);
        i++;
    }
}
numb();
//do
function numbe(){
    let i=1;
    do{
        console.log(i);
        i++;
    }
    while(i<=10);
}
numbe();